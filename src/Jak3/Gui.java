    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        String nombre;
        int vida;
        int atk;
        int derrape;
        int velocidad;
        String tipo;

        nombre = NombreCar.getText();
        vida = Integer.parseInt(formatedTxtVida.getText());
        atk = Integer.parseInt(FormatedTxtAttack.getText());
        derrape = SpinerDerraape.getComponentCount();
        velocidad = Integer.parseInt(FormatedtxtVelocidad.getText());
        tipo = ((String) (TipoCombo.getSelectedItem()));

        if (tipo.equals("Belicos")) {

            Belicos be = new Belicos(velocidad, derrape, nombre, atk, vida);

            cars.add(be);
            AP.setCar(cars);
            AP.escribirArchivo();
            AP.cargarArchivo();
            cars = AP.getCar();
            JOptionPane.showMessageDialog(this, "Se ha agregado con exito");
        }
        if (tipo.equals("Salto")) {

            Salto sal = new Salto(40, velocidad, derrape, nombre, atk, vida);
            cars.add(sal);
            AP.setCar(cars);
            AP.escribirArchivo();
            AP.cargarArchivo();
            cars = AP.getCar();
            JOptionPane.showMessageDialog(this, "Se ha agregado con exito");
        }
        if (tipo.equals("Ataque")) {

            Ataque atke = new Ataque(velocidad, derrape, nombre, atk, vida);
            cars.add(atke);
            AP.setCar(cars);
            AP.escribirArchivo();
            AP.cargarArchivo();
            cars = AP.getCar();

            JOptionPane.showMessageDialog(this, "Se ha agregado con exito");

        }
        if (tipo.equals("Normal")) {

            Vehiculos vh = new Vehiculos(velocidad, derrape, nombre, atk, vida);

            cars.add(vh);
            AP.setCar(cars);
            AP.escribirArchivo();
            AP.cargarArchivo();
            cars = AP.getCar();
            ActualizarCombos();
            JOptionPane.showMessageDialog(this, "Se ha agregado con exito");

        }

        NombreCar.setText("");
        formatedTxtVida.setText("");
        FormatedTxtAttack.setText("");
        FormatedtxtVelocidad.setText("");
        ActualizarCombos();

    }//GEN-LAST:event_jButton4ActionPerformed

